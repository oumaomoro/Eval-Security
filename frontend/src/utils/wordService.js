export const getWordBlob = (contract) => {
  const title = `Redlined Contract - ${contract.vendor_name || 'Document'}`;

  let html = `
    <html xmlns:o='urn:schemas-microsoft-com:office:office' xmlns:w='urn:schemas-microsoft-com:office:word' xmlns='http://www.w3.org/TR/REC-html40'>
    <head><meta charset='utf-8'><title>${title}</title></head>
    <body style="font-family: Arial, sans-serif; max-width: 800px; margin: 0 auto; line-height: 1.6;">
      <h1 style="color: #0f172a; border-bottom: 2px solid #3b82f6; padding-bottom: 10px;">Costloci Redline Export</h1>
      <h2 style="color: #334155;">Vendor: ${contract.vendor_name || 'N/A'}</h2>
      <div style="background-color: #f8fafc; padding: 15px; border-radius: 5px; margin-bottom: 20px;">
        <p style="margin: 0; color: #475569;"><strong>Jurisdiction:</strong> ${contract.detected_jurisdiction || 'Global Standard'}</p>
        <p style="margin: 0; color: #475569;"><strong>Sector:</strong> ${contract.detected_sector || 'General Business'}</p>
        <p style="margin: 0; color: #475569;"><strong>Compliance Score:</strong> ${contract.ai_analysis?.compliance_readiness || 0}%</p>
      </div>
      <p style="color: #64748b; font-size: 11px; font-style: italic;">Auto-generated by Costloci Enterprise AI — Optimized for ${contract.detected_jurisdiction || 'International Law'}</p>
      <hr style="border: none; border-top: 1px solid #cbd5e1; margin: 30px 0;" />
      
      <h2 style="color: #0f172a;">Identified Risks & Suggested Redlines</h2>
  `;

  const findings = contract.ai_analysis?.categorized_findings || [];

  if (findings.length === 0) {
    html += `<p>No significant risks or redlines identified in this document.</p>`;
  }

  findings.forEach((finding, index) => {
    const isCritical = finding.severity === 'critical';

    html += `
      <div style="margin-bottom: 30px; border-bottom: 1px dashed #cbd5e1; padding-bottom: 20px;">
        <h3 style="color: ${isCritical ? '#dc2626' : '#d97706'}; font-size: 16px; margin-bottom: 5px;">
          ${index + 1}. ${finding.title} [${finding.category.toUpperCase()}]
        </h3>
        <p style="margin-top: 5px; color: #475569;"><strong>Risk Analysis:</strong> ${finding.description}</p>
    `;

    if (finding.verbatim_text) {
      html += `
        <h4 style="color: #991b1b; font-size: 14px; margin-top: 15px; margin-bottom: 5px;">Original Clause (Review & Remove):</h4>
        <div style="background-color: #fef2f2; color: #7f1d1d; padding: 15px; border-left: 4px solid #ef4444; margin-bottom: 15px; font-family: 'Courier New', Courier, monospace; font-size: 12px; white-space: pre-wrap;">
          <del>${finding.verbatim_text}</del>
        </div>
      `;
    }

    if (finding.gold_standard_alignment?.suggested_redline) {
      html += `
        <h4 style="color: #166534; font-size: 14px; margin-bottom: 5px;">Suggested Redline (Review & Insert):</h4>
        <div style="background-color: #f0fdf4; color: #14532d; padding: 15px; border-left: 4px solid #22c55e; font-family: 'Courier New', Courier, monospace; font-size: 12px; white-space: pre-wrap;">
          <b><ins>${finding.gold_standard_alignment.suggested_redline}</ins></b>
        </div>
      `;

      if (finding.gold_standard_alignment.gap_analysis) {
        html += `<p style="font-size: 12px; color: #64748b; margin-top: 10px;"><em>AI Note: ${finding.gold_standard_alignment.gap_analysis}</em></p>`;
      }
    }

    html += `</div>`;
  });

  html += `
    </body>
    </html>
  `;

  return new Blob(['\ufeff', html], { type: 'application/msword' });
};

export const exportRedlinesToWord = (contract) => {
  const blob = getWordBlob(contract);
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = `${(contract.vendor_name || 'Document').replace(/\s+/g, '_')}_Costloci_Redlines.doc`;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  URL.revokeObjectURL(url);
};
